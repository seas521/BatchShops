public class ExtAttrNameValue {
    /**
     * SPU id
     */
    private Long  goodsSeriesId ;
    /**
     * 类目ID
     */
    private Long categoryId ;
    /**
     * 扩展属性名称ID
     */
    private Long  extendsAttrId ;
    /**
     * 扩展属性名称
     */
    private String extendsAttrName ;
    /**
     * 扩展属性值
     */
    private String extendsAttrValue ;
    /**
     * 扩展属性值ID
     */
   private String extendsAttrValueId ;

    /**
     * 目标扩展属性和值关系的ID,
     */
   private Long eavrId ;


    private Long extendedAttributeCategoryId ;

    public Long getGoodsSeriesId() {
        return goodsSeriesId;
    }

    public void setGoodsSeriesId(Long goodsSeriesId) {
        this.goodsSeriesId = goodsSeriesId;
    }

    public Long getExtendsAttrId() {
        return extendsAttrId;
    }

    public void setExtendsAttrId(Long extendsAttrId) {
        this.extendsAttrId = extendsAttrId;
    }

    public String getExtendsAttrName() {
        return extendsAttrName;
    }

    public void setExtendsAttrName(String extendsAttrName) {
        this.extendsAttrName = extendsAttrName;
    }

    public String getExtendsAttrValue() {
        return extendsAttrValue;
    }

    public void setExtendsAttrValue(String extendsAttrValue) {
        this.extendsAttrValue = extendsAttrValue;
    }

    public String getExtendsAttrValueId() {
        return extendsAttrValueId;
    }

    public void setExtendsAttrValueId(String extendsAttrValueId) {
        this.extendsAttrValueId = extendsAttrValueId;
    }

    public Long getCategoryId() {
        return categoryId;
    }

    public void setCategoryId(Long categoryId) {
        this.categoryId = categoryId;
    }

    public Long getEavrId() {
        return eavrId;
    }

    public void setEavrId(Long eavrId) {
        this.eavrId = eavrId;
    }

    public Long getExtendedAttributeCategoryId() {
        return extendedAttributeCategoryId;
    }

    public void setExtendedAttributeCategoryId(Long extendedAttributeCategoryId) {
        this.extendedAttributeCategoryId = extendedAttributeCategoryId;
    }
}

package com.if2c.harald.beans;

import java.util.Date;

public class Geavr {
    private Long id;

    private Long goodsSeriesId;

    private Long extendedAttributeValueRelationId;

    private Integer status;

    private Date createdDate;

      public Geavr(Long goodsSeriesId,Long extendedAttributeValueRelationId,Integer status ){
          this.goodsSeriesId = goodsSeriesId ;
          this.extendedAttributeValueRelationId = extendedAttributeValueRelationId ;
          this.status = status ;
      }

    public Long getGoodsSeriesId() {
        return goodsSeriesId;
    }

    public void setGoodsSeriesId(Long goodsSeriesId) {
        this.goodsSeriesId = goodsSeriesId;
    }


    public Integer getStatus() {
        return status;
    }

    public void setStatus(Integer status) {
        this.status = status;
    }

    public Date getCreatedDate() {
        return createdDate;
    }

    public void setCreatedDate(Date createdDate) {
        this.createdDate = createdDate;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getExtendedAttributeValueRelationId() {
        return extendedAttributeValueRelationId;
    }

    public void setExtendedAttributeValueRelationId(Long extendedAttributeValueRelationId) {
        this.extendedAttributeValueRelationId = extendedAttributeValueRelationId;
    }
}